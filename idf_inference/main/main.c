static uint16_t brush_buf[64];
static uint8_t canvas[SCREEN_W * SCREEN_H];

static void draw_char(int x, int y, char c, uint16_t color, int scale) {
    if (c < 32 || c > 127) return;
    const uint8_t *bitmap = font8x8[c - 32];
    for (int i = 0; i < 8; i++) {
        for (int j = 0; j < 8; j++) {
            if (bitmap[i] & (0x80 >> j)) {
                if (scale == 1) {
                    screen_draw_pixel(x + j, y + i, color);
                } else {
                    screen_fill_rect(x + j * scale, y + i * scale, scale, scale, color);
                }
            }
        }
    }
}

static void draw_string(int x, int y, const char *s, uint16_t color, int scale) {
    while (*s) {
        draw_char(x, y, *s++, color, scale);
        x += 8 * scale;
    }
}

static bool point_in_display(int x, int y) {
    int dx = x - DISPLAY_CX;
    int dy = y - DISPLAY_CY;
    return dx * dx + dy * dy <= DISPLAY_RADIUS * DISPLAY_RADIUS;
}

static bool point_in_canvas(int x, int y) {
    return x >= CANVAS_X + CANVAS_BORDER && x < CANVAS_X + CANVAS_SIZE - CANVAS_BORDER &&
           y >= CANVAS_Y + CANVAS_BORDER && y < CANVAS_Y + CANVAS_SIZE - CANVAS_BORDER;
}

static bool point_in_clear_button(int x, int y) {
    return x >= CLEAR_BTN_X && x <= CLEAR_BTN_X + CLEAR_BTN_W &&
           y >= CLEAR_BTN_Y && y <= CLEAR_BTN_Y + CLEAR_BTN_H;
}

static void draw_canvas_frame(void) {
    screen_fill_rect(CANVAS_X - 4, CANVAS_Y - 4, CANVAS_SIZE + 8, CANVAS_SIZE + 8, COLOR_CANVAS_HALO);
    screen_fill_rect(CANVAS_X - 1, CANVAS_Y - 1, CANVAS_SIZE + 2, CANVAS_SIZE + 2, COLOR_CANVAS_EDGE);
    screen_fill_rect(CANVAS_X, CANVAS_Y, CANVAS_SIZE, CANVAS_SIZE, COLOR_CANVAS_BG);
}

static void draw_clear_button(void) {
    const int text_w = 5 * 8;
    screen_fill_rect(CLEAR_BTN_X, CLEAR_BTN_Y, CLEAR_BTN_W, CLEAR_BTN_H, COLOR_BUTTON);
    draw_string(CLEAR_BTN_X + (CLEAR_BTN_W - text_w) / 2, CLEAR_BTN_Y + 6, "CLEAR", COLOR_TEXT, 1);
}

static void draw_status_chip(int x, int y, int w, const char *text, uint16_t bg, uint16_t fg) {
    screen_fill_rect(x, y, w, 16, bg);
    draw_string(x + 6, y + 4, text, fg, 1);
}

static void draw_prediction_panel(char prediction, int confidence_pct, bool sure, int latency_ms) {
    char metric[24];
    uint16_t accent = sure ? COLOR_ACCENT : COLOR_WARN;

    screen_fill_rect(TOP_PANEL_X, TOP_PANEL_Y, TOP_PANEL_W, TOP_PANEL_H, COLOR_PANEL);
    screen_fill_rect(58, 24, 28, 36, accent);
    draw_char(64, 34, prediction, COLOR_TEXT, 2);

    draw_string(92, 24, "ON-DEVICE CNN", COLOR_TEXT_DIM, 1);
    draw_string(92, 40, "pred", COLOR_TEXT_DIM, 1);
    draw_char(130, 40, prediction, COLOR_TEXT, 1);

    snprintf(metric, sizeof(metric), "conf %02d%%", confidence_pct);
    draw_string(138, 40, metric, COLOR_TEXT, 1);

    snprintf(metric, sizeof(metric), "lat %dms", latency_ms);
    draw_status_chip(LAT_CHIP_X, CHIP_Y, LAT_CHIP_W, metric, COLOR_PANEL_ALT, COLOR_TEXT);

    snprintf(metric, sizeof(metric), "%dKB", 
             (int)((inference_workspace_bytes() + sizeof(canvas) + 1023) / 1024));
    draw_status_chip(MEM_CHIP_X, CHIP_Y, MEM_CHIP_W, metric, COLOR_PANEL_ALT, COLOR_TEXT);

    draw_status_chip(STATE_CHIP_X, CHIP_Y, STATE_CHIP_W, sure ? "ready" : "scan",
                     sure ? COLOR_ACCENT_SOFT : COLOR_PANEL_ALT, COLOR_TEXT);
}

static void draw_ui_shell(void) {
    screen_fill_screen(COLOR_BG);
    draw_canvas_frame();
    draw_clear_button();
}

static void update_prediction(char prediction, int confidence_pct, bool sure, int latency_ms) {
    draw_prediction_panel(prediction, confidence_pct, sure, latency_ms);
}

static void app_clear(void) {
    memset(canvas, 0, sizeof(canvas));
    draw_ui_shell();
    update_prediction('-', 0, false, 0);
}

static void draw_brush_fast(int x, int y, uint16_t color, bool update_canvas) {
    int bx = x - BRUSH_RADIUS;
    int by = y - BRUSH_RADIUS;
    int bw = BRUSH_RADIUS * 2;
    int bh = BRUSH_RADIUS * 2;

    if (bx < 0) {
        bw += bx;
        bx = 0;
    }
    if (by < 0) {
        bh += by;
        by = 0;
    }
    if (bx + bw > SCREEN_W) bw = SCREEN_W - bx;
    if (by + bh > SCREEN_H) bh = SCREEN_H - by;
    if (bw <= 0 || bh <= 0) return;

    for (int i = 0; i < bh; i++) {
        for (int j = 0; j < bw; j++) {
            int px = bx + j;
            int py = by + i;
            if (!point_in_canvas(px, py)) {
                brush_buf[i * bw + j] = 0;
                continue;
            }

            brush_buf[i * bw + j] = (uint16_t)((color >> 8) | (color << 8));
            if (update_canvas) {
                canvas[py * SCREEN_W + px] = 1;
            }
        }
    }

    screen_draw_rgb565_block(bx, by, bw, bh, brush_buf);
}

static void draw_line(int x0, int y0, int x1, int y1, uint16_t color, bool update_canvas) {
    int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = -abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = dx + dy, e2;
    while (1) {
        draw_brush_fast(x0, y0, color, update_canvas);
        if (x0 == x1 && y0 == y1) break;
        e2 = 2 * err;
        if (e2 >= dy) {
            err += dy;
            x0 += sx;
        }
        if (e2 <= dx) {
            err += dx;
            y0 += sy;
        }
    }
}

void app_main(void) {
    screen_init();
    touch_init();

    ESP_LOGI(TAG, "Initializing App...");

    uint16_t color = RGB565_WHITE;
    uint16_t c_swap = (color >> 8) | (color << 8);
    for (int i = 0; i < 64; i++) brush_buf[i] = c_swap;

    app_clear();

    int tx, ty, last_tx = -1, last_ty = -1;
    bool touching = false;

    while (1) {
        if (touch_read(&tx, &ty)) {
            if (point_in_clear_button(tx, ty)) {
                app_clear();
                touching = false;
                last_tx = -1;
                last_ty = -1;
                vTaskDelay(pdMS_TO_TICKS(200));
                continue;
            }

            if (!point_in_display(tx, ty) || !point_in_canvas(tx, ty)) {
                vTaskDelay(pdMS_TO_TICKS(10));
                continue;
            }

            if (!touching) {
                touching = true;
                last_tx = tx;
                last_ty = ty;
            }

            draw_line(last_tx, last_ty, tx, ty, COLOR_BRUSH, true);
            last_tx = tx;
            last_ty = ty;
        } else {
            if (touching) {
                inference_result_t result;
                int64_t start_us = esp_timer_get_time();
                inference_run(canvas, SCREEN_W, SCREEN_H, UI_TOP_MARGIN, &result);
                int latency_ms = (int)((esp_timer_get_time() - start_us) / 1000);

                if (result.found) {
                    update_prediction(result.ch, result.confidence_pct, result.sure, latency_ms);
                } else {
                    update_prediction('-', 0, false, latency_ms);
                }

                touching = false;
                last_tx = -1;
                last_ty = -1;
            }
        }
        vTaskDelay(pdMS_TO_TICKS(10));
    }
}
